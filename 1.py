# Открываем файл для чтения
with open('c.txt', 'r') as file:
    # Инициализация суммы и счетчика строк
    total_sum = 0
    line_number = 0
    
    # Проходим по каждой строке файла
    for line in file:
        line_number += 1  # Увеличиваем счетчик строк
        number = int(line.strip())  # Преобразуем строку в число
        # Проверяем, является ли номер строки нечетным
        if line_number % 2 != 0:
            total_sum += number  # Суммируем числа под нечетными номерами

# Выводим результат
print(f'Сумма чисел под нечетными номерами: {total_sum}')